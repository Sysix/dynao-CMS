		<h3>Gibt es was zu beachten, wenn dynaoCMS in einem Unterordner installiert ist?</h3>
		<p>Ja!, die .htaccess <code>RewriteBase /</code> zu <code>RewriteBase /unterordner</code> umbennen</p>
		
